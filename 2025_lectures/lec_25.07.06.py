# function defintion

'''
A function is defined:
    1) def: key word, signal the start of a function definition
    2) function name: choose meaningful names that indicates the 
       function's purpose
    3) parentheses: containing any parameters
    4) :, colon
    5) indented code block:
        All the code within the function must be indented, typically 
        by four spaces
    6) return: key word
          (1) if the function needs to return a value, it is done 
              using the return keyword, followed by the value to be returned
          (2) If no return statement is included, the function implicitly 
              returns None
    


'''

